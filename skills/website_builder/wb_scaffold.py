def _render_shipstack_manifest(spec: ProjectSpec) -> dict:
    return {
        "site_name": spec.site_name,
        "project_type": spec.project_type,
        "deploy_target": spec.deploy_target,
        "auth_required": spec.auth_required,
        "use_supabase": spec.use_supabase,
        "features": [feature.__dict__ for feature in spec.features],
        "entities": [entity.__dict__ for entity in spec.entities],
        "api_routes": [route.__dict__ for route in spec.api_routes],
    }


def _render_migration_sql(spec: ProjectSpec) -> str:
    statements: list[str] = []
    for entity in spec.entities:
        columns = entity.columns or ["id uuid primary key", "name text", "created_at timestamptz default now()"]
        statement = ",\n  ".join(columns)
        statements.append(f"create table if not exists {entity.name} (\n  {statement}\n);")
    return "\n\n".join(statements) + "\n"


def scaffold_project(spec: ProjectSpec, project_path: Path) -> dict[str, str]:
    files: dict[str, str] = {}

    _write_json(project_path / "shipstack.project.json", _render_shipstack_manifest(spec))
    files["manifest"] = str(project_path / "shipstack.project.json")

    if spec.project_type == "static_site":
        _write_text(project_path / "index.html", _render_static_html(spec))
        _write_text(project_path / "styles.css", _render_static_css(spec))
        _write_text(project_path / "script.js", _render_static_js())
        _write_json(project_path / "vercel.json", {"version": 2, "public": True})
        files["entrypoint"] = str(project_path / "index.html")
    else:
        _write_json(project_path / "package.json", _render_fullstack_package_json(spec))
        _write_text(project_path / "server.js", _render_fullstack_server(spec))
        _write_text(project_path / "public" / "index.html", _render_fullstack_html(spec))
        _write_text(project_path / "public" / "styles.css", _render_fullstack_css(spec))
        _write_text(project_path / "public" / "app.js", _render_fullstack_js())
        _write_text(project_path / ".env.example", _render_env_example(spec))
        _write_text(project_path / "Dockerfile", _render_dockerfile())
        _write_text(project_path / ".dockerignore", _render_dockerignore())
        _write_json(
            project_path / "railway.json",
            {
                "$schema": "https://railway.com/railway.schema.json",
                "build": {"builder": "DOCKERFILE", "dockerfilePath": "Dockerfile"},
                "deploy": {"startCommand": "npm start", "restartPolicyType": "ON_FAILURE"},
            },
        )
        _write_text(project_path / "README.md", _render_project_readme(spec))
        files["entrypoint"] = str(project_path / "server.js")

    if spec.use_supabase:
        _write_text(project_path / "supabase" / "config.toml", '[project]\nname = "shipstack-generated"\n')
        _write_text(
            project_path / "supabase" / "migrations" / "20260315000000_initial.sql",
            _render_migration_sql(spec),
        )
        files["supabase"] = str(project_path / "supabase")

    return files

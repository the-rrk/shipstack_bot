def _render_fullstack_package_json(spec: ProjectSpec) -> dict:
    return {
        "name": spec.slug,
        "private": True,
        "version": "0.1.0",
        "type": "module",
        "scripts": {
            "dev": "node server.js",
            "start": "node server.js",
        },
        "dependencies": {
            "@supabase/supabase-js": "^2.49.1",
            "cors": "^2.8.5",
            "dotenv": "^17.3.1",
            "express": "^4.21.2",
        },
    }


def _render_fullstack_server(spec: ProjectSpec) -> str:
    routes = "\n".join(
        (
            f"app.{route.method.lower()}('{route.path}', async (_req, res) => {{\n"
            f"  res.json({{ ok: true, route: '{route.path}', summary: '{route.summary}' }});\n"
            "});"
        )
        for route in spec.api_routes
        if route.path != "/api/health"
    )
    return f"""import cors from 'cors';
import dotenv from 'dotenv';
import express from 'express';
import path from 'node:path';
import {{ fileURLToPath }} from 'node:url';
import {{ createClient }} from '@supabase/supabase-js';

dotenv.config();

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const app = express();
const port = Number(process.env.PORT || 3000);
const publicDir = path.join(__dirname, 'public');

const supabaseUrl = process.env.SUPABASE_URL || '';
const supabaseAnonKey = process.env.SUPABASE_ANON_KEY || '';
const supabase = supabaseUrl && supabaseAnonKey
  ? createClient(supabaseUrl, supabaseAnonKey)
  : null;

app.use(cors());
app.use(express.json());
app.use(express.static(publicDir));

app.get('/api/health', (_req, res) => {{
  res.json({{
    ok: true,
    app: '{spec.site_name}',
    authRequired: {str(spec.auth_required).lower()},
    supabaseEnabled: Boolean(supabase),
  }});
}});

app.get('/api/config', (_req, res) => {{
  res.json({{
    siteName: '{spec.site_name}',
    summary: {json.dumps(spec.summary)},
    authRequired: {str(spec.auth_required).lower()},
    entities: {json.dumps([entity.name for entity in spec.entities])},
  }});
}});

{routes}

app.get('*', (_req, res) => {{
  res.sendFile(path.join(publicDir, 'index.html'));
}});

app.listen(port, () => {{
  console.log(`{spec.site_name} listening on ${{port}}`);
}});
"""



def _render_fullstack_js() -> str:
    return """async function refreshStatus() {
  const status = document.getElementById('status');
  try {
    const [healthResponse, configResponse] = await Promise.all([
      fetch('/api/health'),
      fetch('/api/config'),
    ]);
    const health = await healthResponse.json();
    const config = await configResponse.json();
    status.textContent = JSON.stringify({ health, config }, null, 2);
  } catch (error) {
    status.textContent = `Failed to load runtime status: ${error}`;
  }
}

document.getElementById('refreshButton')?.addEventListener('click', refreshStatus);
refreshStatus();
"""


